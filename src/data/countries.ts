// Helper functions
export const getCountryByCode = (code: string): CountryData | undefined => {
  return COUNTRIES.find(country => country.code === code);
};

export const getCountriesAlphabetically = (): CountryData[] => {
  return [...COUNTRIES].sort((a, b) => a.name.localeCompare(b.name));
};

export const getCountriesByPriority = (): CountryData[] => {
  return [...COUNTRIES].sort((a, b) => a.priority - b.priority);
};

export const searchCountries = (query: string, sortBy: 'alphabetical' | 'priority' = 'alphabetical'): CountryData[] => {
  const lowerQuery = query.toLowerCase().trim();
  if (!lowerQuery) {
    return sortBy === 'priority' ? getCountriesByPriority() : getCountriesAlphabetically();
  }
  
  const filtered = COUNTRIES.filter(country => {
    // Search in country name
    if (country.name.toLowerCase().includes(lowerQuery)) {
      return true;
    }
    
    // Search in dial code
    if (country.dialCode.toLowerCase().includes(lowerQuery)) {
      return true;
    }
    
    // Search in search terms
    return country.searchTerms.some(term => 
      term.toLowerCase().includes(lowerQuery)
    );
  });

  return sortBy === 'priority' 
    ? filtered.sort((a, b) => a.priority - b.priority)
    : filtered.sort((a, b) => a.name.localeCompare(b.name));
};

// Convert to SelectOption format for use with SelectInputAdapter
export const getCountrySelectOptions = (sortBy: 'alphabetical' | 'priority' = 'alphabetical'): Array<{
  label: string;
  value: string;
  searchTerms: string[];
  icon?: string;
  customData?: CountryData;
}> => {
  const countries = sortBy === 'priority' ? getCountriesByPriority() : getCountriesAlphabetically();
  
  return countries.map(country => ({
    label: `${country.flagIcon} ${country.name} (${country.dialCode})`,
    value: country.code,
    searchTerms: country.searchTerms,
    icon: country.flagIcon,
    customData: country,
  }));
};
